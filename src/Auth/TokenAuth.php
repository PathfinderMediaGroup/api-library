namespace PathfinderMediaGroup\ApiLibrary\Auth;

class TokenAuth
{
    /**
     * @var string
     */
    protected $token;

    /**
     * TokenAuth constructor.
     *
     * @param string $token
     */
    public function __construct(string $token)
    {
        $this->token = $token;
    }

    /**
     * @param string $token
     */
    public function setToken(string $token)
    {
        $this->token = $token;
    }

    /**
     * @return string
     */
    public function getToken(): string
    {
        return $this->token;
    }

    /**
     * @return string
     */
    public function getRequestToken(): string
    {
        return base64_encode($this->token);
    }
}
